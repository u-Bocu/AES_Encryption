#define SWAP(a, b) a+=b; b=a-b; a-=b;
#define bTIMES2(a) bXORTimes2(a, 0)

void vCipherAES128(byte_t* state, byte_t** keys) {

	const uint32_t NK_WORDS = 4;
	const uint32_t NB_WORDS = 4;
	const uint32_t NR = 10;

	vAddRoundKey(state, keys[0], NB_WORDS, NK_WORDS);

	for (uint8_t round = 1; round < NR; ++round) {
		vSubBytes(state, NB_WORDS, NK_WORDS);
		vShiftRows(state, NB_WORDS, NK_WORDS);
		vMixColumns(state, NB_WORDS, NK_WORDS);

		vAddRoundKey(state, keys[round], NB_WORDS, NK_WORDS);
	}

	vSubBytes(state, NB_WORDS, NK_WORDS);
	vShiftRows(state, NB_WORDS, NK_WORDS);

	vAddRoundKey(state, keys[NR], NB_WORDS, NK_WORDS);
}

void vInvCipherAES128(byte_t* state, byte_t** keys) {

	const uint32_t NK_WORDS = 4;
	const uint32_t NB_WORDS = 4;
	const uint32_t NR = 10;

	vAddRoundKey(state, keys[0], NB_WORDS, NK_WORDS);

	for (uint8_t round = 1; round < NR; ++round) {

		vInvShiftRows(state, NB_WORDS, NK_WORDS);
		vInvSubBytes(state, NB_WORDS, NK_WORDS);

		vAddRoundKey(state, keys[round], NB_WORDS, NK_WORDS);

		vInvMixColumns(state, NB_WORDS, NK_WORDS);
	}

	vInvShiftRows(state, NB_WORDS, NK_WORDS);
	vInvSubBytes(state, NB_WORDS, NK_WORDS);

	vAddRoundKey(state, keys[NR], NB_WORDS, NK_WORDS);
}

